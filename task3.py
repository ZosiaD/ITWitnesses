'''
Convert Between °C and °F.Convert temperatures from one 
system to another.1 - Create a print statement to convert 
18.5° C to °F.2 - Create a print statement to convert -4° F to °C.
'''

x = (18.5 * 1.8) + 32
print('print statement to convert 18.5° C to °F =', x, '°F')
y = ((-4) - 32) / 1.8
print('print statement to convert -4° F to °C =', y, '°C')
